# 2.2.1. Calcular a area de um retangulo.
print('''
-----------------------------------------------------
 CALCULO DE AREA DE RETANGULO
-----------------------------------------------------
''')

largura = float(input('Largura desejada:'))
altura = float(input('Altura desejada:'))

area = largura * altura

print(f'\nA area do retangulo e {area}.\n')